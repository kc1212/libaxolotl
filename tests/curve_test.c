static char* test_random_agreement()
{
	for (int i = 0; i < 500; i++) {
		struct curve_key_pair alice_pair;
		struct curve_key_pair bob_pair;
		mu_assert("", 0 == curve_generate_keypair(&alice_pair));
		mu_assert("", 0 == curve_generate_keypair(&bob_pair));

		unsigned char shared_alice[CURVE_KEY_BYTES_LEN];
		unsigned char shared_bob[CURVE_KEY_BYTES_LEN];
		mu_assert("", 0 == curve_calculate_agreement(&alice_pair.pk, &bob_pair.sk, shared_bob));
		mu_assert("", 0 == curve_calculate_agreement(&bob_pair.pk, &alice_pair.sk, shared_alice));
		mu_assert("", 0 == sodium_memcmp(shared_alice, shared_bob, CURVE_KEY_BYTES_LEN));
	}
	return 0;
}

static char* test_signature()
{
	const unsigned char alice_identity_private[] = {
		0xc0, 0x97, 0x24, 0x84, 0x12,
		0xe5, 0x8b, 0xf0, 0x5d, 0xf4,
		0x87, 0x96, 0x82, 0x05, 0x13,
		0x27, 0x94, 0x17, 0x8e, 0x36,
		0x76, 0x37, 0xf5, 0x81, 0x8f,
		0x81, 0xe0, 0xe6, 0xce, 0x73,
		0xe8, 0x65
	};

	const unsigned char alice_identity_public[] = {
		0x05, 0xab, 0x7e, 0x71, 0x7d,
		0x4a, 0x16, 0x3b, 0x7d, 0x9a,
		0x1d, 0x80, 0x71, 0xdf, 0xe9,
		0xdc, 0xf8, 0xcd, 0xcd, 0x1c,
		0xea, 0x33, 0x39, 0xb6, 0x35,
		0x6b, 0xe8, 0x4d, 0x88, 0x7e,
		0x32, 0x2c, 0x64
	};

	const unsigned char alice_ephemeral_public[] = {
		0x05, 0xed, 0xce, 0x9d, 0x9c,
		0x41, 0x5c, 0xa7, 0x8c, 0xb7,
		0x25, 0x2e, 0x72, 0xc2, 0xc4,
		0xa5, 0x54, 0xd3, 0xeb, 0x29,
		0x48, 0x5a, 0x0e, 0x1d, 0x50,
		0x31, 0x18, 0xd1, 0xa8, 0x2d,
		0x99, 0xfb, 0x4a
	};

	const unsigned char alice_sig[] = {
		0x5d, 0xe8, 0x8c, 0xa9, 0xa8,
		0x9b, 0x4a, 0x11, 0x5d, 0xa7,
		0x91, 0x09, 0xc6, 0x7c, 0x9c,
		0x74, 0x64, 0xa3, 0xe4, 0x18,
		0x02, 0x74, 0xf1, 0xcb, 0x8c,
		0x63, 0xc2, 0x98, 0x4e, 0x28,
		0x6d, 0xfb, 0xed, 0xe8, 0x2d,
		0xeb, 0x9d, 0xcd, 0x9f, 0xae,
		0x0b, 0xfb, 0xb8, 0x21, 0x56,
		0x9b, 0x3d, 0x90, 0x01, 0xbd,
		0x81, 0x30, 0xcd, 0x11, 0xd4,
		0x86, 0xce, 0xf0, 0x47, 0xbd,
		0x60, 0xb8, 0x6e, 0x88
	};

	mu_assert("", CURVE_PUBLIC_SERIALIZED_LEN == sizeof alice_ephemeral_public);
	mu_assert("", CURVE_SIG_BYTES_LEN == sizeof alice_sig);

	struct curve_sk alice_sk;
	struct curve_pk alice_pk;
	struct curve_pk alice_ek; // ephemeral key

	mu_assert("", 0 == curve_decode_private_point(alice_identity_private, sizeof alice_identity_private, &alice_sk));
	mu_assert("", 0 == curve_decode_point(alice_identity_public, 0, &alice_pk));
	mu_assert("", 0 == curve_decode_point(alice_ephemeral_public, 0, &alice_ek));

	unsigned char msg[CURVE_PUBLIC_SERIALIZED_LEN];
	mu_assert("", 0 == curve_serialize_pk(&alice_ek, msg));
	mu_assert("", 0 == sodium_memcmp(alice_ephemeral_public, msg, CURVE_PUBLIC_SERIALIZED_LEN));
	mu_assert("", 0 == curve_verify_signature(&alice_pk, msg, sizeof msg, alice_sig));

	// validation should fail after modifying the signature
	for (int i = 0; i < sizeof alice_sig; i++) {
		unsigned char modified_sig[sizeof alice_sig];
		memcpy(modified_sig, alice_sig, sizeof alice_sig);
		modified_sig[i] ^= 0x01;
		mu_assert("", !!curve_verify_signature(&alice_pk, msg, sizeof msg, modified_sig));
	}

	return 0;
}

static char* test_random_verification()
{
	for (int i = 0; i < 500; i++) {
		struct curve_key_pair alice_pair;
		const size_t mlen = 128;
		unsigned char m[mlen];
		unsigned char sig[CURVE_SIG_BYTES_LEN];

		randombytes_buf(m, mlen);
		mu_assert("", 0 == curve_generate_keypair(&alice_pair));
		mu_assert("", 0 == curve_calculate_signature(&alice_pair.sk, m, mlen, sig));
		mu_assert("", 0 == curve_verify_signature(&alice_pair.pk, m, mlen, sig));
	}
	return 0;
}

static char* all_tests()
{
	mu_run_test(test_key_bytes);
	mu_run_test(test_agreement);
	mu_run_test(test_random_agreement);
	mu_run_test(test_signature);
	mu_run_test(test_random_verification);
	return 0;
}

int main()
{
	char *result = all_tests();
	if (result != 0) {
		printf("%s\n", result);
	}
	printf("Tests run: %d\n", tests_run);

	return result != 0;
}


